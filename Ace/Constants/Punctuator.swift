import Foundation

enum Punctuator: String, CaseIterable {
	case plus = "+"
	case minus = "-"
	case asterisk = "*"
	case slash = "/"
	case upArrow = "^"
	case ampersand = "&"
	case verticalLine = "|"
	case percent = "%"
	case rightArrow = ">"
	case leftArrow = "<"
	case equal = "="
	case exclamationPoint = "!"
	case questionMark = "?"
	case leftParenthesis = "("
	case rightParenthesis = ")"
	case leftBracket = "["
	case rightBracket = "]"
	case leftBrace = "{"
	case rightBrace = "}"
	case coma = ","
	case semicolon = ";"
	case dot = "."
	case colon = ":"
}
